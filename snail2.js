snail = function(array) {
    let n = array.length,
        counter = n * array[0].length,
        res = [],
        row = 0;

    if (n === 1) {
        return array[0]
    }

    while (counter > 0) {
        let temp = array.shift()
        res.push(...temp)
        counter -= temp.length

        if (counter <= 0) break;
        array.forEach((item) => {
            res.push(item.pop())
            counter -=1
        })

        if (counter <= 0) break;
        let temp2 = array.pop()?.reverse()
        res.push(...temp2)
        counter -= temp2?.length

        if (counter <= 0) break;
        for (let i = array.length-1; i > -1; i--) {
            res.push(array[i].shift())
            counter -=1
        }
    }
    return res.flat(3)
}

